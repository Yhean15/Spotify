package Elementos;

public class Song {
    public String Nombre;
    public String Album;
    public String Autor;
    public double duracion;
    public Song(String Nombre, String Album, String artista, double duracion){
        this.Nombre=Nombre;
        this.Album=Album;
        this.Autor=artista;
        this.duracion=duracion;
    }
}
